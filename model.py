import json
import os


# ========== КЛАСС МОДЕЛИ ==========
class Model:
    """Класс модели, хранящий три числа A, B, C.
    Отвечает за соблюдение правила A ≤ B ≤ C.
    Модель сама проверяет и корректирует значения.
    При изменении данных уведомляет всех подписчиков (наблюдателей)."""

    def __init__(self):
        """Конструктор модели.
        Устанавливает начальные значения: A=10, B=50, C=90.
        Создаёт пустой список наблюдателей."""
        self._a = 10                    # значение A (приватное)
        self._b = 50                    # значение B (приватное)
        self._c = 90                    # значение C (приватное)
        self._observers = []            # список подписчиков на изменения

    def add_observer(self, observer):
        """Добавляет подписчика (наблюдателя).
        Подписчик должен иметь метод вызова (например, update_ui).
        При изменении данных все подписчики будут уведомлены."""
        self._observers.append(observer)

    def _notify(self):
        """Уведомляет всех подписчиков об изменении модели.
        Вызывается только после того, как все изменения завершены.
        Это гарантирует, что уведомление будет отправлено один раз."""
        for observer in self._observers:
            observer()

    # ===== ГЕТТЕРЫ (получение значений) =====
    def get_a(self):
        """Возвращает текущее значение A."""
        return self._a

    def get_b(self):
        """Возвращает текущее значение B."""
        return self._b

    def get_c(self):
        """Возвращает текущее значение C."""
        return self._c

    # ===== СЕТТЕРЫ (изменение значений) =====
    def set_a(self, value):
        """Устанавливает новое значение A.
        Разрешающее поведение: при нарушении правила A ≤ B ≤ C
        модель сама подстраивает B и C."""
        # Ограничиваем значение от 0 до 100
        value = max(0, min(100, value))
        old_a = self._a
        self._a = value

        # Если A > B, подтягиваем B вверх
        if self._a > self._b:
            self._b = self._a
        # Если B > C, подтягиваем C вверх
        if self._b > self._c:
            self._c = self._b

        # Уведомляем подписчиков только если значение действительно изменилось
        if self._a != old_a:
            self._notify()

    def set_b(self, value):
        """Устанавливает новое значение B.
        Ограничивающее поведение: значение B ограничивается между A и C.
        Если пользователь ввёл число меньше A или больше C,
        оно заменяется на ближайшее допустимое."""
        # Ограничиваем значение от 0 до 100
        value = max(0, min(100, value))
        old_b = self._b

        # B должно быть между A и C
        self._b = max(self._a, min(self._c, value))

        # Уведомляем подписчиков только если значение изменилось
        if self._b != old_b:
            self._notify()

    def set_c(self, value):
        """Устанавливает новое значение C.
        Разрешающее поведение: при нарушении правила A ≤ B ≤ C
        модель сама подстраивает A и B."""
        # Ограничиваем значение от 0 до 100
        value = max(0, min(100, value))
        old_c = self._c
        self._c = value

        # Если C < B, подтягиваем B вниз
        if self._c < self._b:
            self._b = self._c
        # Если B < A, подтягиваем A вниз
        if self._b < self._a:
            self._a = self._b

        # Уведомляем подписчиков только если значение изменилось
        if self._c != old_c:
            self._notify()

    # ===== СОХРАНЕНИЕ И ЗАГРУЗКА =====
    def save(self, filename="data.json"):
        """Сохраняет текущие значения A, B, C в файл JSON.
        Вызывается при закрытии приложения."""
        data = {"a": self._a, "b": self._b, "c": self._c}
        with open(filename, "w") as f:
            json.dump(data, f)

    def load(self, filename="data.json"):
        """Загружает значения A, B, C из файла JSON.
        Вызывается при запуске приложения.
        Если файл не существует, используются значения по умолчанию."""
        if os.path.exists(filename):
            with open(filename, "r") as f:
                data = json.load(f)
                self._a = data.get("a", 10)      # если нет значения, берём 10
                self._b = data.get("b", 50)      # если нет значения, берём 50
                self._c = data.get("c", 90)      # если нет значения, берём 90
                self._notify()                   # уведомляем об обновлении
